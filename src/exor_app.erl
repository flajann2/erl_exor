-module('exor_app').
-include_lib("amqp_client/include/amqp_client.hrl").

-behaviour(application).

%% Application callbacks
-export([start/2
        ,stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    io:fwrite("app start called!!!\n\n"),
    'exor_sup':start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    io:fwrite("app stop called!!!\n\n"),
    ok.

